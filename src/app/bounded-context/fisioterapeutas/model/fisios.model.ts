export class FisiosModel {
  public id:any;
  public nombre:any;
  public imagen:any;
  public biografia:any;
  public edad:any;
  public horario:any;

  constructor(id:any, nombre:any, imagen:any, biografia:any, edad:any, horario:any) {
    this.id = id;
    this.nombre = nombre;
    this.imagen = imagen;
    this.biografia = biografia;
    this.edad = edad;
    this.horario = horario;
  }
}
